<?php

var_dump(10 == "10"); // sama dengan (true)
var_dump(10 === "10"); // sama dengan identik (false)
var_dump(10 != "10"); // tidak sama dengan (false)
var_dump(10 !== "10"); // tidak sama dengan identik (true)
var_dump(10 < 9); // kurang dari (false)
var_dump(10 <= 9); // kurang dari atau sama dengan (false)
var_dump(9 >= 9); // lebih dari atau sama dengan (true)
var_dump(9 > 7); // lebih dari (true)